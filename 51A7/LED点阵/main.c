#include "LED.h"

u8 gled_col[32]=
{0x40,0x10,0x20,0x10,0x10,0x10,0x08,0x10,0x0C,0x10,0x0A,0x10,0x09,0xFF,0x08,0x10,
0x08,0x10,0x08,0x10,0x08,0x11,0x08,0x10,0x08,0x10,0x08,0x12,0x08,0x14,0x08,0x18};

u8 gled_row[32]=
{0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};


void main()
{
	u8 i=0;
	while(1)
	{		
		for(i=0;i<16;i++)
		{			
			HC595_write_data(gled_row[i],gled_row[i+16],~gled_col[2*i],~gled_col[2*i+1]);
			_nop_();
			HC595_write_data(0,0,0,0);
		}

	}
}